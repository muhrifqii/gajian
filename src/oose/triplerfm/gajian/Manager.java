package oose.triplerfm.gajian;

public class Manager {
	
	private int id;
	private String name;
	public Manager(){
		
	}
	public Manager(String name){
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
